SET NAMES UTF8;
DROP DATABASE IF EXISTS Fs;
CREATE DATABASE Fs CHARSET=UTF8;
USE Fs;
####用户表单
CREATE TABLE fs_user(
	uid INT PRIMARY KEY AUTO_INCREMENT,
	uname VARCHAR(32),
	upwd VARCHAR(32),
	mail VARCHAR(32),
	card VARCHAR(16),
	time VARCHAR(16),
	ip VARCHAR(32)
);
#####论坛信息
CREATE TABLE fs_txt(
	uid INT PRIMARY KEY AUTO_INCREMENT,
	title VARCHAR(36),
	author VARCHAR(16),
	reply INT(16),
	amount INT(16),
	publication VARCHAR(16),
	a VARCHAR(16),
	b VARCHAR(16)
);
